//GET USER STATS

router.get("/stats", verifyTokenAndAdmin, async (req, res) => {
  const date = new Date();
  const lastYear = new Date(date.setFullYear(date.getFullYear() - 1));
console.log(lastYear)//return date of one year ago
  try {
    const data = await User.aggregate([
      //match is condition and it will check
      { $match: { createdAt: { $gte: lastYear } } },
      //month variable will take month from every matched condition
      //if september month=0
      {
        $project: {
          month: { $month: "$createdAt" },
        },
      },
      //now grouping
      {
        $group: {
          _id: "$month",
          total: { $sum: 1 },
        },
      },
    ]);
    res.status(200).json(data)
  } catch (err) {
    res.status(500).json(err);
  }
});

